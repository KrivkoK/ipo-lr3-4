# Функция для проверки существования треугольника
def can_form_triangle(a, b, c):
    return (a + b > c) and (a + c > b) and (b + c > a)

# Запрос чисел у пользователя
try:
    side1 = float(input("Введите первую сторону треугольника: "))
    side2 = float(input("Введите вторую сторону треугольника: "))
    side3 = float(input("Введите третью сторону треугольника: "))

    # Проверка существования треугольника
    if can_form_triangle(side1, side2, side3):
        print("Треугольник с такими сторонами существует.")
    else:
        print("Треугольник с такими сторонами не существует.")
except ValueError:
    print("Пожалуйста, введите действительные числа.")
